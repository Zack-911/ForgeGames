export function getWord(difficulty: WordDifficulty): string {
  const list =
    difficulty === 'easy' ? EASY_WORDS : difficulty === 'hard' ? HARD_WORDS : MEDIUM_WORDS

  return list[Math.floor(Math.random() * list.length)]!
}

export function scramble(word: string): string {
  const arr = word.split('')
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[arr[i], arr[j]] = [arr[j]!, arr[i]!]
  }
  // guarantee the scramble is different from the original
  if (arr.join('') === word && word.length > 1) return scramble(word)
  return arr.join('')
}

/** Returns emoji tiles for a Wordle guess */
export function wordleResult(secret: string, guess: string): string {
  const len = secret.length
  const result: string[] = Array(len).fill('⬛')
  const secretArr = secret.split('')
  const guessArr = guess.split('')
  const used = Array(len).fill(false)

  // Pass 1: correct position
  for (let i = 0; i < len; i++) {
    if (guessArr[i] === secretArr[i]) {
      result[i] = '🟩'
      used[i] = true
      guessArr[i] = ''
    }
  }

  // Pass 2: right letter wrong position
  for (let i = 0; i < len; i++) {
    if (!guessArr[i]) continue
    const idx = secretArr.findIndex((c, j) => c === guessArr[i] && !used[j])
    if (idx !== -1) {
      result[i] = '🟨'
      used[idx] = true
    }
  }

  return result.join('')
}
